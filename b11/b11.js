let number = -5.5;
console.log(Math.abs(number));
// tính giá trị tuyệt đối của 1 số bất kì |number|

let number2 = 4.4;
let number3 = 5.5;
console.log(Math.ceil(number2));
console.log(Math.ceil(number3));
// làm tròn số lên đến giá trị nguyên gần nhất 

let number4 = 5.7;
let number5 = 6.1;
console.log(Math.floor(number4));
console.log(Math.floor(number5));
// làm tròn số xuống đến giá trị nguyên gần nhất

let number6 = 10.1;
let number7 = 20.5;
console.log(Math.round(number6));
console.log(Math.round(number7));
// nếu số thập phân >= 5 thì làm tròn lên giá trị nguyên gần nhất
// nếu số thập phân < 5 thì làm tròn xuống giá trị nguyên gần nhất
 




